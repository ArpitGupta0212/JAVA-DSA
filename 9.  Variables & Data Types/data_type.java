public class data_type {
    public static void main(String args[]){
    byte b=8;
    //only 256 numbers only store in byte type {-128 to 127} size 1 byte
    System.out.println(b);
    short sh=7;
    // size of short 2bytes
    System.out.println(sh);
    char ch='a';
    // size=2 byte all alphabets and characters
    System.out.println(ch);
    boolean bo=true;
    // size= 1 byte only true and false
    System.out.println(bo);
    int num=10;
    // size=4 Byte -2billion to 2 billion
    System.out.println(num);
    float myboat=2.5f;
    // size=4 byte
    System.out.println(myboat);
    long lo=90;
    // size=8 byte 
    System.out.println(lo);
    double dou=5.67;
    // size=8 byte
    System.out.println(dou);
}}
