public class switch_num {
    public static void main(String args[]){
        int num=3;
        switch(num){
            case 1:System.out.println(num);
            break;
            case 2:System.out.println(num);
            break;
            case 3:System.out.println(num);
            break;
            default :System.out.println(num);
        }
    }
}
