public class bitwise_operation{
    public static void main(String args[]){
        // bitwise and *
        System.out.println(5&6);
        // bitwise Or +
        System.out.println(5|6);
        // bitwise xor same =0
        System.out.println(5^6);
        // bitwise one's coplement
        System.out.println(~5);
        // bitwise left shift a<<b b is how many space u want shift (a*2**b)
        System.out.println(5<<2);
        // bitwise right shift a>>b b is how many space u want shift (a/2**b)
        System.out.println(6>>1);
    }
}