public class relational_operator {
    public static void main(String args[]){
        int a=89;
        int b=07;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}
